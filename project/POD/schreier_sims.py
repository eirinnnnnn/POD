def sift_remainder_right(g: np.ndarray, chain, n: int):
    """
    Right-coset sifting remainder.

    Returns residual r after removing subgroup part from the RIGHT using transversals.

    Property:
        For any h in H, sift_remainder_right(g ∘ h) == sift_remainder_right(g).
    """
    r = g.copy()
    for level in chain:
        b = level["base_point"]
        image = int(r[b])
        trans = level["trans"]

        if image not in trans:
            break

        t = trans[image]                 # t(b) = image
        r = compose(r, inverse(t))       # r <- r ∘ t^{-1}

    return r


def right_coset_key(g: np.ndarray, chain, n: int):
    """
    Hashable key for right coset gH using right sifting remainder.
    """
    rem = sift_remainder_right(g, chain, n)
    return tuple(int(x) for x in rem.tolist())


# ================================================================
# I/O helpers
# ================================================================

def load_generators_from_json(path):
    """
    Load generator permutations from a JSON file produced by part_gen_set_eBCH.py.
    """
    with open(path, "r") as f:
        data = json.load(f)

    n = data["n"]
    H_perms = data["H_perms"]
    gens = [np.array(p, dtype=int) for p in H_perms]
    return n, gens, data


# ================================================================
# Main CLI
# ================================================================

def main():
    parser = argparse.ArgumentParser(
        description="Run Schreier–Sims on an eBCH automorphism generator set JSON."
    )
    parser.add_argument(
        "json_path",
        help="Path to JSON file containing H_perms (e.g., aut_generators_eBCH_m4_t2.json)."
    )
    parser.add_argument(
        "--base",
        type=str,
        default=None,
        help="Comma-separated list of base points (e.g. '0,1,2'). "
             "If omitted, uses 0..n-1 until stabilizer becomes trivial."
    )

    args = parser.parse_args()

    # Load generator set
    n, gens, meta = load_generators_from_json(args.json_path)
    print(f"[info] Loaded generator set from {args.json_path}")
    print(f"       n = {n}, k = {meta.get('k', '?')}, |H| (raw gens) = {len(gens)}")

    # Determine base
    if args.base is not None:
        base = [int(x) for x in args.base.split(",") if x.strip() != ""]
    else:
        # naive base: [0, 1, 2, ..., n-1]
        base = list(range(n))

    print(f"[info] Using base B = {base}")

    # Run Schreier–Sims
    chain = schreier_sims(gens, base, n)

    print("\n[info] Schreier–Sims chain summary:")
    for i, level in enumerate(chain, start=1):
        b = level["base_point"]
        orbit_size = len(level["orbit"])
        num_gens = len(level["gens"])
        print(f"  Level {i}: base_point = {b}, |orbit| = {orbit_size}, |gens(G^{({i})})| ≈ {num_gens}")

    order = group_order_from_chain(chain)
    print(f"\n[info] Estimated group order |G| = {order}")

    # Small demo: test membership of each original generator
    print("\n[info] Membership test of original generators:")
    for idx, g in enumerate(gens):
        ok = sift_membership(g, chain, n)
        print(f"  gen #{idx}: in group? {ok}")
    # ============================================================
    # Save results to JSON
    # ============================================================

    # Collect full SGS from chain (union of all level generators)
    sgs = []
    sgs_seen = set()
    for level in chain:
        for g in level["gens"]:
            key = tuple(g.tolist())
            if key not in sgs_seen and not is_identity(g):
                sgs_seen.add(key)
                sgs.append(g.tolist())

    # Prepare JSON-serializable structure
    json_out = {
        "file_source": args.json_path,
        "n": n,
        "base": base,
        "group_order": int(order),
        "num_levels": len(chain),
        "levels": [],
        "sgs": sgs,
    }

    # for i, level in enumerate(chain, start=1):
    #     json_out["levels"].append({
    #         "level": i,
    #         "base_point": int(level["base_point"]),
    #         "orbit": [int(x) for x in level["orbit"]],
    #         "orbit_size": int(len(level["orbit"])),
    #         "num_gens": int(len(level["gens"])),
    #     })
    for i, level in enumerate(chain, start=1):
        # Store transversals aligned with orbit order (compact + JSON-friendly)
        orbit_list = [int(x) for x in level["orbit"]]
        trans_dict = level["trans"]  # dict[int -> np.ndarray]
        trans_list = [trans_dict[x].tolist() for x in orbit_list]

        json_out["levels"].append({
            "level": i,
            "base_point": int(level["base_point"]),
            "orbit": orbit_list,
            "orbit_size": int(len(orbit_list)),
            "num_gens": int(len(level["gens"])),
            "trans": trans_list,   # trans[j] maps base_point -> orbit[j]
        })


    # Output path
    out_path = args.json_path.replace(".json", "_schreier_sims.json")

    with open(out_path, "w") as f:
        json.dump(json_out, f, indent=2)

    print(f"\n[info] Schreier–Sims results saved → {out_path}")



if __name__ == "__main__":
    main()
